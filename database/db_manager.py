def import_database(src_path):
    """Заменяет текущую БД файлом из src_path, затем переинициализирует движок."""
    import shutil
    global ENGINE, _Session, _migrated
    from sqlalchemy import create_engine as _ce
    try:
        ENGINE.dispose()
        shutil.copy2(src_path, _DB_PATH)
        db_url = 'sqlite:///' + _DB_PATH.replace('\\', '/')
        ENGINE = _ce(db_url, echo=False)
        _Session = sessionmaker(bind=ENGINE)
        _migrated = False  # сбрасываем флаг — новая БД может требовать миграции
    except Exception as e:
        raise RuntimeError(f"Не удалось импортировать базу данных:\n{_DB_PATH}\n{e}")

