def search_by_ingredients(ingredient_list: list) -> list:
    """Возвращает рецепты содержащие хотя бы один из ингредиентов."""
    from sqlalchemy.orm import joinedload
    import re as _re

    # Словарь синонимов и корней для поиска
    SYNONYMS = {
        "рыба": ["судак","треска","лосось","сёмга","форель","тунец","осетрина","стерлядь",
                 "дорада","скумбрия","сельдь","окунь","карп","щука","горбуша","минтай"],
        "мясо": ["говядина","свинина","баранина","телятина","курица","индейка","утка","фарш"],
        "говядина": ["говядин"],
        "свинина": ["свинин","свиной"],
        "курица": ["курин","куриц","курица"],
        "рис": ["рис","рисов"],
        "картофель": ["картофел","картошк"],
        "помидор": ["помидор","томат"],
        "сыр": ["сыр","сырн"],
        "яйцо": ["яиц","яйц","яйко"],
        "лук": ["лук","луков","луковиц"],
        "чеснок": ["чеснок","чесноч"],
        "грибы": ["гриб","шампиньон","грибов"],
        "паста": ["паст","макарон","спагетт","лапш","феттучин","тальятелл"],
        "морепродукты": ["креветк","кальмар","мидий","краб","осьминог","гребешк"],
    }

    def _matches_ingredient(query: str, ings_text: str) -> bool:
        q = query.lower().strip()
        # Прямое вхождение
        if q in ings_text:
            return True
        # Поиск через синонимы
        syns = SYNONYMS.get(q, [q])
        return any(s in ings_text for s in syns)

    session = get_session()
    results = []
    for r in session.query(Recipe).options(joinedload(Recipe.chef)).all():
        ings_text = (r.ingredients or "").lower()
        matches = sum(1 for ing in ingredient_list if _matches_ingredient(ing, ings_text))
        if matches > 0:
            chef = r.chef
            results.append({
                "id": r.id, "title": r.title,
                "chef": chef.name if chef else "", "chef_country": chef.country if chef else "🌍",
                "cuisine": r.cuisine_type, "difficulty": r.difficulty,
                "total_time": r.total_time, "rating": r.rating,
                "emoji": r.image_emoji, "image_path": r.image_path or "",
                "ingredients": json.loads(r.ingredients) if r.ingredients else [],
                "instructions": json.loads(r.instructions) if r.instructions else [],
                "tags": [], "prep_time": r.prep_time, "cook_time": r.cook_time,
                "step_timers": [], "rating_count": r.rating_count,
                "is_favorite": r.is_favorite, "is_user_added": r.is_user_added,
                "calories_per_serving": r.calories_per_serving or 0,
                "proteins_per_serving": r.proteins_per_serving or 0,
                "fats_per_serving":     r.fats_per_serving or 0,
                "carbs_per_serving":    r.carbs_per_serving or 0,
                "default_servings":     r.default_servings or 4,
                "cook_count":           r.cook_count or 0,
                "_matches": matches,
            })
    session.close()
    results.sort(key=lambda x: x["_matches"], reverse=True)
    return results



