def _split_ingredients(text: str) -> list:
    """Разбивает строку вида 'Картофель, лук, морковь' на отдельные ингредиенты."""
    # Если в строке есть количество — не разбиваем (это один ингредиент)
    if re.search(r'\d', text):
        return [text]
    # Разбиваем по запятой
    parts = [p.strip() for p in text.split(',')]
    return [p for p in parts if p]


def _grams_from_text(text: str) -> float:
    text_lo = text.lower().strip()

    # Паттерн: число + единица (500г, 1.5кг, 200мл, 3 ст.л.)
    m = re.search(
        r'(\d+[.,]?\d*)\s*'
        r'(кг|г\b|мг|л\b|мл|дл|ст\.л\.?|ч\.л\.?|стакан\w*|чашк\w*|'
        r'шт\.?\b|штук\w*|пучк\w*|горст\w*|щепотк\w*|зубчик\w*|зуб\b|'
        r'долька\w*|ломтик\w*|кусочк\w*|кусок|банк\w*|упаковк\w*|пакет\w*)',
        text_lo)
    if m:
        val  = float(m.group(1).replace(',', '.'))
        unit = m.group(2).rstrip('.')
        # Штуки — ищем вес одной штуки
        if unit.startswith('шт') or unit.startswith('штук'):
            for key, w in PIECE_WEIGHT.items():
                if key in text_lo:
                    return val * w
            return val * 100
        for k, factor in UNITS.items():
            if unit.startswith(k) or k.startswith(unit):
                return val * factor
        return val

    # Просто число в начале строки (без единицы) → штуки
    m2 = re.match(r'^(\d+[.,]?\d*)\s+', text_lo)
    if m2:
        count = float(m2.group(1).replace(',', '.'))
        for key, w in PIECE_WEIGHT.items():
            if key in text_lo:
                return count * w
        return count * 100

    # Нет числа → типичная порция продукта
    for key, w in PIECE_WEIGHT.items():
        if key in text_lo:
            return w  # один экземпляр
    return 80  # fallback


def _find_food(text_lo: str):
    best_key, best_len = None, 0
    for key in FOOD_DB:
        if key in text_lo and len(key) > best_len:
            best_key, best_len = key, len(key)
    return FOOD_DB[best_key] if best_key else None


def calculate_nutrition(ingredients: list, servings: int = 4) -> dict:
    """Принимает список строк ингредиентов, возвращает КБЖУ на 1 порцию."""
    total_kcal = total_p = total_f = total_c = 0.0

    for ing_raw in ingredients:
        # Разбиваем "Картофель, лук, морковь" на отдельные строки
        parts = _split_ingredients(ing_raw)
        for ing in parts:
            ing_lo = ing.lower().strip()
            if len(ing_lo) < 2:
                continue

            # Пропускаем чисто специи/приправы без калорий
            if re.match(r'^(соль|перец|лавр|специ|пряност|зелень|укроп|петрушк|базилик|тимьян|розмарин|орегано|зира|кориандр|паприк|кардамон|гвоздик|корица)\b', ing_lo):
                continue

            food = _find_food(ing_lo)
            if food is None:
                continue

            grams = _grams_from_text(ing)
            if grams <= 0:
                grams = 80

            kcal, p, f, c = food
            factor = grams / 100.0
            total_kcal += kcal * factor
            total_p    += p    * factor
            total_f    += f    * factor
            total_c    += c    * factor

    srv = max(servings, 1)
    return {
        "calories": round(total_kcal / srv),
        "proteins": round(total_p    / srv, 1),
        "fats":     round(total_f    / srv, 1),
        "carbs":    round(total_c    / srv, 1),
    }


def calories_for_servings(recipe: dict, servings: int) -> dict:
    if recipe.get("calories_per_serving", 0) > 0:
        base   = recipe.get("default_servings", 4) or 4
        factor = servings / base
        return {
            "calories": round(recipe["calories_per_serving"] * factor),
            "proteins": round(recipe.get("proteins_per_serving", 0) * factor, 1),
            "fats":     round(recipe.get("fats_per_serving", 0) * factor, 1),
            "carbs":    round(recipe.get("carbs_per_serving", 0) * factor, 1),
        }
    import json
    ings = json.loads(recipe.get("ingredients", "[]")) if isinstance(recipe.get("ingredients"), str) else recipe.get("ingredients", [])
    return calculate_nutrition(ings, servings)
